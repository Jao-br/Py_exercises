# Char Count in String 

def char_count():
    string = input("Digite a palavra que deseja contabilizar os caracteres: ")

    char_num = len(string)

    return char_num

char_num = char_count()
print(f"O total de caracteres da frase/palavra digitada é: {char_num}")

